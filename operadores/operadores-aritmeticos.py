### Operadores Aritméticos
# Suma y resta
suma = 10 + 5
resta = 10 - 5

print("Suma: " + f"{suma}")
print("Resta: " + f"{resta}")

# Multiplicación y división
multi = 10 * 5
division = 10 / 5 # devuelve un float

print("Multiplicación: " + f"{multi}")
print("División: " + f"{division}")

# Potenciación (exponente) (**)
exponente = 10 ** 5
print("Exponente: " + f"{exponente}")

# División baja (//): Devuelve el entero de la división
division_baja = 11 // 5
print("División Baja: " + f"{division_baja}")

# Resto o módulo (%)
resto = 11 % 5

print("Resto: " + f"{resto}")

tipo_de_dato = type(division_baja) #type() devuelve el tipo de dato que es
print("Tipo de dato de la división baja: " + f"{tipo_de_dato}")

tipo_de_dato = type(division) #type() devuelve el tipo de dato que es
print("Tipo de dato de la división: " + f"{tipo_de_dato}")
